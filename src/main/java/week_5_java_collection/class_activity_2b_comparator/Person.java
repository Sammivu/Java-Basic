package week_5_java_collection.class_activity_2b_comparator;

public class Person {
    private String fullName;

    private  int age;

    public Person(String fullName, int age) {
        this.fullName = fullName;
        this.age = age;
    }

    public String getFullName() {
        return fullName;
    }

    public int getAge() {
        return age;
    }
}
